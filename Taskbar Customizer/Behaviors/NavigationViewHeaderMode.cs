/// <summary>
/// Specifies the mode of the header in a NavigationView.
/// </summary>
public enum NavigationViewHeaderMode
{
    /// <summary>
    /// The header is always displayed.
    /// </summary>
    Always,

    /// <summary>
    /// The header is never displayed.
    /// </summary>
    Never,

    /// <summary>
    /// The header is displayed minimally.
    /// </summary>
    Minimal,
}
